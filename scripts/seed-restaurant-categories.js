async function seedCategoryPages() {
  console.log(`\nSeeding ${categories.length} restaurant category pages for ${DESTINATION_ID}...\n`)

  for (const cat of categories) {
    const row = {
      destination_id: DESTINATION_ID,
      source_table: 'restaurants',
      slug: cat.slug,
      title: cat.title,
      subtitle: cat.subtitle,
      description: cat.description,
      source_category_field: cat.source_category_field,
      source_category_value: cat.source_category_value,
      intro_content: cat.intro_content,
      faq: cat.faq,
      meta_title: cat.meta_title,
      meta_description: cat.meta_description,
      sort_order: cat.sort_order,
      published: true,
      show_filters: false,
      items_per_page: 50,
    }

    const { error } = await supabase
      .from('category_pages')
      .upsert(row, { onConflict: 'destination_id,slug' })

    if (error) {
      console.error(`✗ ${cat.slug}: ${error.message}`)
    } else {
      console.log(`✓ ${cat.slug} — ${cat.title}`)
    }
  }

  const { count } = await supabase
    .from('category_pages')
    .select('*', { count: 'exact', head: true })
    .eq('destination_id', DESTINATION_ID)
    .eq('source_table', 'restaurants')
    .eq('published', true)

  console.log(`\nTotal category_pages for ${DESTINATION_ID} (restaurants, published): ${count}\n`)
}

seedCategoryPages().catch(err => {
  console.error('Fatal:', err)
  process.exit(1)
})
